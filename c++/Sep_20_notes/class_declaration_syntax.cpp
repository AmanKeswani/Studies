#include <iostream>

using namespace std;

class class_name { // Here "class" is a needed keyword, class_name is any name 
                   // of the class which cannot start with a symbol or a number.
    
    private:       // access specifier
    int a;         // variable declaration
    void abc();    // function declaration

    public:
    int b;         // variable declaration
    void bc();     // function declaration

};                 // Semi colon to end the class declaration